//! Generate `docs/error-codes.md` from `aube_codes::errors::ALL` +
//! `warnings::ALL`. Run via `cargo run -p aube-codes --bin generate-error-codes-docs`
//! (wired into `mise run render` alongside the settings doc generator).
//!
//! The output is fully reproducible from the registry — every code's
//! identifier, category, description, and exit code lives next to its
//! `pub const` declaration in `crates/aube-codes/src/{errors,warnings}.rs`.
//! Hand-edits to `docs/error-codes.md` will be clobbered on the next
//! `mise run render`. Update the registry instead.

use aube_codes::{CodeMeta, errors, warnings};
use std::collections::BTreeMap;
use std::fmt::Write as _;
use std::fs;
use std::path::{Path, PathBuf};

const COMMAND: &str = "cargo run -p aube-codes --bin generate-error-codes-docs";

fn main() {
    let root = workspace_root();
    let out_path = root.join("docs/error-codes.md");
    let markdown = render_page();
    if let Some(parent) = out_path.parent() {
        fs::create_dir_all(parent)
            .unwrap_or_else(|e| panic!("failed to create {}: {e}", parent.display()));
    }
    fs::write(&out_path, markdown)
        .unwrap_or_else(|e| panic!("failed to write {}: {e}", out_path.display()));
    println!(
        "generated {}",
        out_path.strip_prefix(&root).unwrap().display()
    );
}

fn workspace_root() -> PathBuf {
    Path::new(env!("CARGO_MANIFEST_DIR"))
        .parent()
        .unwrap()
        .parent()
        .unwrap()
        .to_path_buf()
}

fn render_page() -> String {
    let mut out = String::new();
    out.push_str(&format!(
        "<!-- Auto-generated by `{COMMAND}`. Do not edit by hand — \
         your changes will be clobbered on the next `mise run render`. \
         Update `crates/aube-codes/src/errors.rs` or `warnings.rs` instead. -->\n\n"
    ));
    out.push_str(PREAMBLE);
    out.push_str("\n## Errors\n\n");
    render_section(&mut out, errors::ALL, /*include_exit=*/ true);
    out.push_str("\n## Warnings\n\n");
    out.push_str(WARNINGS_PREAMBLE);
    render_section(&mut out, warnings::ALL, /*include_exit=*/ false);
    out.push_str(ADDING_A_CODE);
    out
}

fn render_section(out: &mut String, all: &[CodeMeta], include_exit: bool) {
    // Group by category, preserving the order categories first appear
    // in `ALL`. Within each category, codes are emitted in declaration
    // order — same order they appear in the registry — so the page
    // matches the source layout.
    let mut groups: Vec<(&'static str, Vec<&CodeMeta>)> = Vec::new();
    let mut category_index: BTreeMap<&'static str, usize> = BTreeMap::new();
    for meta in all {
        let idx = match category_index.get(&meta.category) {
            Some(i) => *i,
            None => {
                let i = groups.len();
                groups.push((meta.category, Vec::new()));
                category_index.insert(meta.category, i);
                i
            }
        };
        groups[idx].1.push(meta);
    }

    for (category, entries) in groups {
        writeln!(out, "### {category}\n").unwrap();
        if include_exit {
            writeln!(out, "| Code | Exit | Description |").unwrap();
            writeln!(out, "|------|------|-------------|").unwrap();
            for meta in entries {
                let exit = match meta.exit_code {
                    Some(c) => format!("`{c}`"),
                    None => "`1`".to_string(),
                };
                writeln!(
                    out,
                    "| `{}` | {} | {} |",
                    meta.name,
                    exit,
                    escape_table_cell(meta.description)
                )
                .unwrap();
            }
        } else {
            writeln!(out, "| Code | Description |").unwrap();
            writeln!(out, "|------|-------------|").unwrap();
            for meta in entries {
                writeln!(
                    out,
                    "| `{}` | {} |",
                    meta.name,
                    escape_table_cell(meta.description)
                )
                .unwrap();
            }
        }
        writeln!(out).unwrap();
    }
}

/// Markdown table cells use `|` as the column separator and treat
/// raw newlines as row breaks. None of the descriptions in `ALL`
/// currently contain either, but this keeps the generator robust if
/// someone adds one later. Newlines collapse to a space; `|` is
/// escaped with a backslash.
fn escape_table_cell(s: &str) -> String {
    s.replace('\n', " ").replace('|', "\\|")
}

const PREAMBLE: &str = r#"# Error and warning codes

aube emits a stable string identifier with every error and most
warnings. Codes look like `ERR_AUBE_NO_LOCKFILE` or
`WARN_AUBE_IGNORED_BUILD_SCRIPTS`, and they're attached as a
structured field — no regex on stderr required.

This page lists every code aube currently emits, what it means, and
(for errors) the bespoke Unix exit code it produces.

## How to read codes

**Default text output**: errors include the code in their
miette-rendered output, e.g. `× foo (ERR_AUBE_NO_LOCKFILE)`. Warnings
include the code as a structured field after the message.

**ndjson output** (`aube --reporter ndjson <cmd>`): every record carries
a `code` field. Branch on `code == "ERR_AUBE_..."` instead of
substring-matching the human message.

```jsonc
{
  "level": "WARN",
  "code": "WARN_AUBE_IGNORED_BUILD_SCRIPTS",
  "count": 2,
  "packages": ["esbuild", "opencode-ai"],
  "message": "ignored build scripts for 2 package(s): esbuild, opencode-ai. ..."
}
```

**Exit codes**: errors exit with `1` (generic) by default. The errors
called out as having a bespoke exit in the table below get a stable
numeric exit code instead, so shell scripts can branch without parsing
stderr.

## Naming

- `ERR_AUBE_*` — fatal errors. Process exits non-zero.
- `WARN_AUBE_*` — non-fatal warnings. Install continues; the warning
  is informational.

aube does not emit `ERR_PNPM_*` codes. Where a code maps onto a pnpm
concept (lockfile, peer-deps, tarball, etc.) the suffix matches pnpm's
naming so the meaning is obvious to anyone familiar with pnpm's
[error page](https://pnpm.io/errors), but aube reserves its own
prefix so the codes can evolve independently.

## Stability

Once published, a code's identifier and meaning don't change. New
codes can be added at any time. Removing or repurposing a code is a
breaking change.

Bespoke exit codes follow the same contract. The exit-code allocation
is grouped by category — see [`crates/aube-codes/src/exit.rs`][exit-src]
for the full layout — but consumers should branch on the exit *value*,
not the category, since categories are documentation, not API.

[exit-src]: https://github.com/endevco/aube/blob/main/crates/aube-codes/src/exit.rs
"#;

const WARNINGS_PREAMBLE: &str = r#"Codes prefixed `WARN_AUBE_*` indicate non-fatal conditions. The
install proceeds; the warning surfaces something the user may want to
act on. Bespoke exit codes do not apply to warnings.

"#;

const ADDING_A_CODE: &str = r#"
## Adding a code

1. Add a `pub const` to `crates/aube-codes/src/errors.rs` or
   `warnings.rs`.
2. Add a [`CodeMeta`] entry to the local `ALL` slice carrying the
   category, one-line description, and (for errors) optional bespoke
   exit code. The self-tests in `crates/aube-codes/src/lib.rs` and
   `exit.rs` reject typos, missing descriptions, duplicate exit
   codes, and out-of-range exits.
3. Reference the constant from the call site:
   - For warnings: `tracing::warn!(code = aube_codes::warnings::WARN_AUBE_X, ...)`.
   - For thiserror enums: `#[diagnostic(code(ERR_AUBE_X))]`.
   - For ad-hoc miette: `miette::miette!(code = aube_codes::errors::ERR_AUBE_X, ...)`.
4. Run `mise run render` to regenerate this page.
"#;
